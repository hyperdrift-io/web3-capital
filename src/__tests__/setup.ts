import '@testing-library/jest-dom'

// porto's Dialog module calls window.matchMedia during connector setup.
// jsdom doesn't implement it — provide a no-op stub.
Object.defineProperty(window, 'matchMedia', {
  writable: true,
  value: (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  }),
})
